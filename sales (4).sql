SET @INVENT=(SELECT COUNT(*) FROM SS_INVMONTHLY WHERE (LOT_NUMBER=NEW.LOT_NUMBER) AND (MATERIAL_CODE=NEW.MATERIAL_CODE) AND (STORAGE = NEW.STORAGE) AND (STATUS=NEW.STATUS));
IF @INVENT=0 THEN
INSERT INTO SS_INVMONTHLY (YYMM,COMPANY,PLANT,MATERIAL_CODE,LOT_NUMBER,BEGIN_QTY,IN_QTY,OUT_QTY,END_QTY,STORAGE,STATUS) VALUES (DATE_FORMAT(NOW(),'%Y%m'),NEW.COMPANY,NEW.PLANT,NEW.MATERIAL_CODE,NEW.LOT_NUMBER,0,NEW.IN_DAILY_QTY,NEW.OUT_DAILY_QTY,(BEGIN_QTY+IN_QTY)-OUT_QTY,NEW.STORAGE,NEW.STATUS);
ELSE
UPDATE SS_INVMONTHLY SET IN_QTY = IN_QTY + NEW.in_daily_qty, OUT_QTY = OUT_QTY + NEW.out_daily_qty, END_QTY = (IN_QTY+BEGIN_QTY) - OUT_QTY
WHERE LOT_NUMBER = NEW.lot_number AND MATERIAL_CODE = NEW.material_code AND STORAGE= NEW.STORAGE AND STATUS= NEW.STATUS;
END IF;
END
